//Assignment Operators

let c=10;

console.log("Assigning to 2:",c=2); //assignment

console.log("Addition with 2:",c+=2); //addition

console.log("Subtraction with 2:",c-=2); //subtraction

console.log("Multiplication with 2:",c*=2); //multiplication

console.log("Division with 2:",c/=2); //division

console.log("Modulus with 2:",c%=2); //remainder

console.log("Exponential with 2:",c**=2); //power of
