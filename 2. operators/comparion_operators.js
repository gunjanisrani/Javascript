//Comparion operators

let d=10;
let e=5;

console.log("Equal",d==e);
console.log("Not Equal",d!=e);
console.log("Equal to (including datatype)",d===e);
console.log("Not Equal to (including datatype)",d!==e);
console.log("Greater than",d>e);
console.log("Greater than equal to",d>=e);
console.log("Less than",d<e);
console.log("Less than equal to",d<=e);
